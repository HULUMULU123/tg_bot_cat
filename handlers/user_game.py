def register_user_game_handlers(bot: TeleBot, db: Database) -> None:
    @bot.message_handler(commands=["start"])
    def start_command(message: Message) -> None:
        user_id = message.from_user.id
        db.ensure_user(user_id)
        if db.is_legal_accepted(user_id):
            bot.send_message(
                chat_id=message.chat.id,
                text=WELCOME_TEXT,
                reply_markup=main_menu_keyboard(db.is_notify_enabled(user_id)),
            )
            return
        bot.send_message(
            chat_id=message.chat.id,
            text=LEGAL_TEXT,
            reply_markup=legal_accept_keyboard(),
        )

    @bot.callback_query_handler(func=lambda call: call.data == "legal_accept")
    def accept_legal(call: CallbackQuery) -> None:
        user_id = call.from_user.id
        db.set_legal_accepted(user_id)
        bot.answer_callback_query(call.id, text="–°–ø–∞—Å–∏–±–æ! –î–æ—Å—Ç—É–ø –æ—Ç–∫—Ä—ã—Ç.")
        bot.edit_message_text(
            chat_id=call.message.chat.id,
            message_id=call.message.message_id,
            text=WELCOME_TEXT,
            reply_markup=main_menu_keyboard(db.is_notify_enabled(user_id)),
        )

    @bot.callback_query_handler(func=lambda call: call.data == "notify_toggle")
    def toggle_notifications(call: CallbackQuery) -> None:
        user_id = call.from_user.id
        enabled = not db.is_notify_enabled(user_id)
        db.set_notify(user_id, enabled)
        if enabled:
            bot.answer_callback_query(call.id, text="üîî –£–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –≤–∫–ª—é—á–µ–Ω—ã")
            bot.send_message(
                chat_id=call.message.chat.id,
                text="üîî –£–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –≤–∫–ª—é—á–µ–Ω—ã\n–Ø —Å–æ–æ–±—â—É, –∫–æ–≥–¥–∞ –Ω–∞—á–Ω—ë—Ç—Å—è –°–±–æ–π.",
            )
        else:
            bot.answer_callback_query(call.id, text="üîï –£–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –≤—ã–∫–ª—é—á–µ–Ω—ã")
            bot.send_message(
                chat_id=call.message.chat.id,
                text="üîï –£–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –≤—ã–∫–ª—é—á–µ–Ω—ã.",
            )
        reply_markup = call.message.reply_markup
        is_menu = False
        enter_url = None
        if reply_markup and getattr(reply_markup, "keyboard", None):
            for row in reply_markup.keyboard:
                for button in row:
                    if getattr(button, "callback_data", None) in MENU_CONTENT:
                        is_menu = True
                    if getattr(button, "url", None):
                        enter_url = button.url
        if is_menu:
            new_markup = main_menu_keyboard(enabled)
        else:
            new_markup = notification_keyboard(
                notify_on=enabled,
                show_enter=enter_url is not None,
                enter_url=enter_url,
            )
        bot.edit_message_reply_markup(
            chat_id=call.message.chat.id,
            message_id=call.message.message_id,
            reply_markup=new_markup,
        )

    @bot.callback_query_handler(func=lambda call: call.data in MENU_CONTENT)
    def menu_callback(call: CallbackQuery) -> None:
        user_id = call.from_user.id
        if not db.is_legal_accepted(user_id):
            bot.answer_callback_query(
                call.id,
                text="–ß—Ç–æ–±—ã –æ—Ç–∫—Ä—ã—Ç—å –∏–≥—Ä—É, –Ω—É–∂–Ω–æ –æ–∑–Ω–∞–∫–æ–º–∏—Ç—å—Å—è —Å –ø—Ä–∞–≤–∏–ª–∞–º–∏.",
                show_alert=True,
            )
            bot.send_message(
                chat_id=call.message.chat.id,
                text=LEGAL_TEXT,
                reply_markup=legal_accept_keyboard(),
            )
            return
        bot.answer_callback_query(call.id)
        content = MENU_CONTENT.get(call.data, WELCOME_TEXT)
        bot.edit_message_text(
            chat_id=call.message.chat.id,
            message_id=call.message.message_id,
            text=content,
            reply_markup=main_menu_keyboard(db.is_notify_enabled(user_id)),
        )
